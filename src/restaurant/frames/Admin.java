    private void btnEditMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditMenuActionPerformed
        if (!tblMenu.getSelectionModel().isSelectionEmpty()) {
            Integer selectedRow = tblMenu.getSelectedRow();
            
            MenuObject tobeEdit = modelMenu.get(selectedRow);
            
            MenuDialog dialog = new MenuDialog(this, true, -1, tobeEdit);
            
            dialog.setVisible(true);
            
            if (dialog.isSaved && dialog.isEdit) {
                MenuObject toUpdate = new MenuObject(dialog.id, dialog.nama, dialog.kategori, dialog.harga, dialog.stock);

                modelMenu.update(selectedRow, toUpdate);
            }
        }
    }//GEN-LAST:event_btnEditMenuActionPerformed


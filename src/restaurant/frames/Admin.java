    private void btnHapusKasirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusKasirActionPerformed
        if (!tblKasir.getSelectionModel().isSelectionEmpty()) {
            KasirObject tbRemove = modelKasir.remove(tblKasir.getSelectedRow());
            
            if (tbRemove != null) {
                btnHapusKasir.setEnabled(false);
                btnEditKasir.setEnabled(false);
                btnReset.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnHapusKasirActionPerformed

    private void btnEditKasirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditKasirActionPerformed
        if (!tblKasir.getSelectionModel().isSelectionEmpty()) {
            Integer selectedRow = tblKasir.getSelectedRow();
            
            KasirObject tobeEdit = modelKasir.get(selectedRow);
            
            KasirDialog dialog = new KasirDialog(this, true, tobeEdit);
            
            dialog.setVisible(true);
            
            if (dialog.isEdit && dialog.isSaved) {
                KasirObject toUpdate = new KasirObject(dialog.nama, dialog.username, dialog.password, null);
                
                modelKasir.update(selectedRow, toUpdate);
            }
        }
    }//GEN-LAST:event_btnEditKasirActionPerformed
    

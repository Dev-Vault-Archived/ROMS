    private void hitungKembalian() {
        kembalian = (Integer) jsUang.getValue() - totalHarga;
        
        lblKembalian.setText(rupiah(kembalian));
        
        if (kembalian >= 0) {
            btnSimpan.setEnabled(true);
        } else {
            btnSimpan.setEnabled(false);
        }
    }
    
    private void listValueChanged(Integer i, javax.swing.event.ListSelectionEvent evt, javax.swing.JList<String> e, DefaultListModel<String> m) {
        if (e.getSelectedIndex() != -1 && evt.getValueIsAdjusting() == false) {
            String dataOrder = m.get(e.getSelectedIndex());
            
            String[] dataOrderArray = dataOrder.split("#");
            
            selectedMenu.put(i, menuData.get(Integer.parseInt(dataOrderArray[1]) - 1));
            
            selectedMenuPrice[i] = selectedMenu.get(i).getHarga();
            
            calculateResultTotal();
            
            if (!jsUang.isEnabled()) {
                jsUang.setEnabled(true);
            }
        }
    }
    
    private String rupiah(Integer rupiah) {
        return NumberFormat.getCurrencyInstance(new Locale("id", "ID")).format(rupiah);
    }
    
    private void calculateResultTotal() {
        Integer th = 0;
        for (Integer i : selectedMenuPrice) {
            th += i;
        }
        
        totalHarga = th;
        
        lblTotalHarga.setText(rupiah(totalHarga));
        
        hitungKembalian();
    }
    

    public PesananDialog(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        
        this.setLocationRelativeTo(parent);
        
        initComponents();
        
        pelayanData.addAll(PelayanModel.getPelayan());
        tableData.addAll(TableModel.getTable());
        menuData.addAll(MenuModel.getMenu(true));
        
        pelayanData.forEach((p) -> {
            pilihPelayan.addItem(p.getKode() + " - " + p.getNama());
        });
        
        tableData.forEach((p) -> {
            pilihMeja.addItem(p.getKode());
        });
        
        Integer i = 1;
        
        for (MenuObject p : menuData) {
            switch (p.getKategori()) {
                case 0:
                    listPembukaModel.addElement(p.getNama() + " ( "+ rupiah(p.getHarga()) +" ) - #" + i);
                    break;
                case 1:
                    listUtamaModel.addElement(p.getNama() + " ( "+ rupiah(p.getHarga()) +" ) - #" + i);
                    break;
                case 2:
                    listPenutupModel.addElement(p.getNama() + " ( "+ rupiah(p.getHarga()) +" ) - #" + i);
                    break;
                case 3:
                    listMinumanModel.addElement(p.getNama() + " ( "+ rupiah(p.getHarga()) +" ) - #" + i);
                    break;
            }
            
            i++;
        }
        
        listPembuka.setModel(listPembukaModel);
        listUtama.setModel(listUtamaModel);
        listPen.setModel(listPenutupModel);
        listMinuman.setModel(listMinumanModel);
    }


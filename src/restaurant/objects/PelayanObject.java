package restaurant.objects;

/**
 *
 * @author askaeks
 */
public final class PelayanObject {
    private String kode;
    private String nama;

    public PelayanObject(String kode, String nama) {
        setKode(kode);
        setNama(nama);
    }
    
    /**
     * @return the kode
     */
    public String getKode() {
        return kode;
    }

    /**
     * @param kode the kode to set
     */
    public void setKode(String kode) {
        this.kode = kode;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }
}
